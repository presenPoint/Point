export type MessageKey = keyof typeof en;

const TABLES: Record<AppLocale, Record<MessageKey, string>> = {
  en: en as unknown as Record<MessageKey, string>,
  ko,
};

export function isMessageKey(key: string): key is MessageKey {
  return Object.prototype.hasOwnProperty.call(en, key);
}

export function translateStoredMessage(
  locale: AppLocale,
  text: string | null | undefined,
  vars?: Record<string, string | number>,
): string | null {
  if (!text) return null;
  return isMessageKey(text) ? getMessage(locale, text, vars) : text;
}

export function getMessage(locale: AppLocale, key: MessageKey, vars?: Record<string, string | number>): string {
  let s = TABLES[locale][key] ?? TABLES.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}
